# 3° Crie um programa que receba 3 valores e print se é positivo ou negativo;

n1 = int(input("Informe um valor: "))
n2 = int(input("Informe outro valor: "))
n3 = int(input("Informe outro valor: "))

if n1 > 0:
    print("O primeiro número é positivo")
else:
    print("O primeiro número é negativo")

if n2 > 0:
    print("O segundo número é positivo")
else:
    print("O segundo número é negativo") 

if n3 > 0:
    print("O terceiro número é positivo")
else:
    print("O terceiro número é negativo")

